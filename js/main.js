document.addEventListener('keydown', function(e) {
    // ESC key to go back
    if (e.key === 'Escape') {
        if (currentSection === 'golpo') {
            const readingContainer = document.getElementById('storyReadingContainer');
            const storyList = document.getElementById('storyListContainer');

            if (!readingContainer.classList.contains('hidden')) {
                backToStoryList();
            } else if (!storyList.classList.contains('hidden')) {
                backToGolpoSubcategories();
            }
        } else if (currentSection === 'uponash') {
            const readingPage = document.getElementById('novelReadingPage');
            if (!readingPage.classList.contains('hidden')) {
                backToNovelList();
            }
        } else if (currentSection === 'shayari') {
            const content = document.getElementById('shayariContent');
            const langSelection = document.getElementById('languageSelection');

            if (!content.classList.contains('hidden')) {
                backToLanguageSelection();
            } else if (!langSelection.classList.contains('hidden')) {
                backToShayariCategories();
            }
        }
    }
});

