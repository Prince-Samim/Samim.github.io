function showGolpoStories(category) {
    const subcategories = document.getElementById('golpoSubcategories');
    const storyListContainer = document.getElementById('storyListContainer');
    const storyGrid = document.getElementById('storyGrid');
    const categoryTitle = document.getElementById('storyCategoryTitle');

    // Hide subcategories, show story list
    subcategories.classList.add('hidden');
    storyListContainer.classList.remove('hidden');

    // Set category title
    const titles = {
        'vuter': 'ভূতের গল্প',
        'romantic': 'রোমান্টিক লাভ স্টোরি',
        'sad': 'স্যাড লাভ স্টোরি'
    };
    categoryTitle.textContent = titles[category];

    // Clear and populate story grid
    storyGrid.innerHTML = '';
    const stories = storiesData[category] || [];

    stories.forEach((story, index) => {
        const card = document.createElement('div');
        card.className = 'story-card reveal';
        card.style.animationDelay = `${index * 0.1}s`;
        card.innerHTML = `
            <h4>${story.title}</h4>
            <p>${story.preview}</p>
            <button class="read-more-btn" onclick="readStory('${category}', ${story.id})">
                Read More <i class="fas fa-arrow-right"></i>
            </button>
        `;
        storyGrid.appendChild(card);
    });

    setTimeout(setupScrollReveal, 100);
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

