function readStory(category, storyId) {
    const story = storiesData[category].find(s => s.id === storyId);
    if (!story) return;

    document.getElementById('storyListContainer').classList.add('hidden');
    document.getElementById('storyReadingContainer').classList.remove('hidden');

    document.getElementById('storyTitle').textContent = story.title;
    document.getElementById('storyAuthor').textContent = `লেখক: ${story.author}`;
    document.getElementById('storyDate').textContent = story.date;
    document.getElementById('storyContent').innerHTML = story.content;

    window.scrollTo({ top: 0, behavior: 'smooth' });
}

